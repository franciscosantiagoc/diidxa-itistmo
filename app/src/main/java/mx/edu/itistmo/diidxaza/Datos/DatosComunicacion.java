package mx.edu.itistmo.diidxaza.Datos;

public class DatosComunicacion {
    private String esp;
    private String zap;

    public DatosComunicacion(String esp, String zap) {
        this.esp = esp;
        this.zap = zap;
    }

    public String getEsp() { return esp; }

    public String getZap() {
        return zap;
    }

}
